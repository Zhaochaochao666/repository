package com.zth.model;

public class User {
	
	private int us_id;
	private String us_name;
	private int us_sex;
	private int us_age;
	
	public int getUs_id() {
		return us_id;
	}
	public void setUs_id(int us_id) {
		this.us_id = us_id;
	}
	public String getUs_name() {
		return us_name;
	}
	public void setUs_name(String us_name) {
		this.us_name = us_name;
	}
	public int getUs_sex() {
		return us_sex;
	}
	public void setUs_sex(int us_sex) {
		this.us_sex = us_sex;
	}
	public int getUs_age() {
		return us_age;
	}
	public void setUs_age(int us_age) {
		this.us_age = us_age;
	}
	
}
